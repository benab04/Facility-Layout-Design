    # Define the Departments and their required Area
    departments = ['Receiving', 'Milling', 'Press', 'Assembly', 'Shipping']
    areas = {
        'Receiving': 4,
        'Milling': 6,
        'Press': 8,
        'Assembly': 6,
        'Shipping': 4
    }

    # Define the AEIOUX Relationship Matrix
    rel_matrix = [
        ['U', 'A', 'I', 'O', 'U'], # Receiving relations
        ['A', 'U', 'E', 'I', 'O'], # Milling relations
        ['I', 'E', 'U', 'A', 'U'], # Press relations
        ['O', 'I', 'A', 'U', 'A'], # Assembly relations
        ['U', 'O', 'U', 'A', 'U']  # Shipping relations
    ]

    facility_width = 4

    # Run ALDEP
    aldep = ALDEP(departments, areas, rel_matrix, facility_width)

    # Generate Sequence
    seq = aldep.generate_sequence(mdp_letter='I')
    print("Placement Sequence:", seq)

    # Place Departments (Sweep Method)
    aldep.place_departments(seq)

    # Calculate Score
    score = aldep.calculate_layout_score()
    print("Layout Score:", score)

    # Visualize
    aldep.plot_layout(score)